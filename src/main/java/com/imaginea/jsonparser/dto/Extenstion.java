package com.imaginea.jsonparser.dto;

import java.util.List;

public class Extenstion {

	private String url; 
	
	private List<URLExtenstion> urlExtensions;

	public String getUrl() {
		return url;
	}

	public void setUrl(String url) {
		this.url = url;
	}

	public List<URLExtenstion> getUrlExtensions() {
		return urlExtensions;
	}

	public void setUrlExtensions(List<URLExtenstion> urlExtensions) {
		this.urlExtensions = urlExtensions;
	}
	
	
	
}
